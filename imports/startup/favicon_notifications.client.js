$(window).on('focus blur mousemove', (e) => {

    if(e.type == 'blur'){
        // on window blur, remove the window_focused boolean
        Session.set( 'window_focused', false );
    }
    else {
        // on window focus, reset the favicon notification
        // to zero an activate the window_focused boolean
        Session.set( 'window_focused', true );
        Session.set( 'favicon_notif', 0 );
    }
        
});